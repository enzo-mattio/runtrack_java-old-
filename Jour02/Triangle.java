package Jour02;

import java.util.Scanner;

public class Triangle {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    System.out.println("Entrez un nombre :");
    int num = sc.nextInt();
    for (int i = 0; i < num; i++) {
      
      System.out.println("*".repeat(i+1));
    
  }
}
}
